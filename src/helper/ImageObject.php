<?php
namespace Keepa\helper;

class ImageObject
{
    /**
     * Filename of the large image.
     * @var string|null
     */
    public $l = null;

    /**
     * Height of the large image, in pixels.
     * @var int|null
     */
    public $lH = null;

    /**
     * Width of the large image, in pixels.
     * @var int|null
     */
    public $lW = null;

    /**
     * Filename of the medium image.
     * @var string|null
     */
    public $m = null;

    /**
     * Height of the medium image, in pixels.
     * @var int|null
     */
    public $mH = null;

    /**
     * Width of the medium image, in pixels.
     * @var int|null
     */
    public $mW = null;
}
